package com.zap.main.pojo;

public class ZapUserPojo 
{
	
	String firstname;
	String lastname;
	String username;
	String email;
	String password;
	String confirmpassword;
	String country;
	String phoneno;
	
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getConfirmpassword() {
		return confirmpassword;
	}
	public void setConfirmpassword(String confirmpassword) {
		this.confirmpassword = confirmpassword;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getPhoneno() {
		return phoneno;
	}
	public void setPhoneno(String phoneno) {
		this.phoneno = phoneno;
	}
	@Override
	public String toString() {
		return "ZapUserPojo [firstname=" + firstname + ", lastname=" + lastname + ", username=" + username + ", email="
				+ email + ", password=" + password + ", confirmpassword=" + confirmpassword + ", country=" + country
				+ ", phoneno=" + phoneno + "]";
	}
	
	
	
	
}
